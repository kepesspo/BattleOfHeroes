import Foundation
import UIKit

extension UIFont {
    class func helveticaNeueLight(_ size: CGFloat) -> UIFont {
        return UIFont(name: "HelveticaNeue-Light", size: size)!
    }
    
    class func rubic(_ size: CGFloat) -> UIFont {
        return UIFont(name: "Rubik-Medium", size: size)!
    }
}

